#!/usr/bin/env node
/**
 * car-master 번들 빌드
 *
 * public/data/car-master/*.json (개별 차종 카탈로그) + _index.json 을
 * 단일 _bundle.js 로 묶어 file:// 더블클릭 환경에서도 로드 가능하게 한다.
 *
 * 사용:
 *   node scripts/build-car-master-bundle.cjs
 *   또는 npm run build:car-master
 */
const fs = require('fs');
const path = require('path');

const DIR = path.resolve(__dirname, '..', 'public', 'data', 'car-master');
const OUT = path.join(DIR, '_bundle.js');

if (!fs.existsSync(DIR)) {
  console.error(`[build-car-master-bundle] not found: ${DIR}`);
  process.exit(1);
}

const indexPath = path.join(DIR, '_index.json');
if (!fs.existsSync(indexPath)) {
  console.error(`[build-car-master-bundle] _index.json missing`);
  process.exit(1);
}

const idx = JSON.parse(fs.readFileSync(indexPath, 'utf-8'));
const all = {};
const files = fs.readdirSync(DIR).filter(f => f.endsWith('.json') && f !== '_index.json').sort();

for (const file of files) {
  const id = file.replace(/\.json$/, '');
  all[id] = JSON.parse(fs.readFileSync(path.join(DIR, file), 'utf-8'));
}

const banner = `/* AUTO-GENERATED by scripts/build-car-master-bundle.cjs — DO NOT EDIT */\n`;
const out = banner
  + `window.CAR_MASTER_INDEX=${JSON.stringify(idx)};\n`
  + `window.CAR_MASTER=${JSON.stringify(all)};\n`;

fs.writeFileSync(OUT, out);
const sizeKb = (out.length / 1024).toFixed(1);
console.log(`[build-car-master-bundle] ${OUT} (${files.length} catalogs, ${sizeKb} KB)`);
